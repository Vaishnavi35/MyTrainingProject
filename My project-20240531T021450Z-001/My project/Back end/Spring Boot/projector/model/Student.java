package com.projector.model;

public class Student {
	
	public String id;
	public String name;
	public String gender;
	public String dob;
	public String std;
	public String score1;
	public String score2;
	public String practical1;
	public String practical2;
	public String username;
	public String password;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getStd() {
		return std;
	}
	public void setStd(String std) {
		this.std = std;
	}
	public String getScore1() {
		return score1;
	}
	public void setScore1(String score1) {
		this.score1 = score1;
	}
	public String getScore2() {
		return score2;
	}
	public void setScore2(String score2) {
		this.score2 = score2;
	}
	public String getPractical1() {
		return practical1;
	}
	public void setPractical1(String practical1) {
		this.practical1 = practical1;
	}
	public String getPractical2() {
		return practical2;
	}
	public void setPractical2(String practical2) {
		this.practical2 = practical2;
	}
	

	
	
}
